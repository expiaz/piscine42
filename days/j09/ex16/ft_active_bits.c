unsigned int	ft_active_bits(int value)
{
	int		n;
	int		i;

	i = 0;
	n = 0;
	while (i < 32)
	{
		if (value % 2 == 1)
			n++;
		value /= 2;
		i++;
	}
	return (n);
}
